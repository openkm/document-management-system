package com.openkm.frontend.client.bean;

/**
 * Coordenates of al the widgets to make consistent resizes 
 * and agrupate position and size values.
 *
 * @author jllort
 */
public class Coordenates {
	private int x = 0;
	private int y = 0;
	private int width = 0;
	private int height = 0;

	public Coordenates() {
	}

	public Coordenates(int x, int y, int width, int height) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}

	public void setX(int x) {
		this.x = x;
	}

	public void setY(int y) {
		this.y = y;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getX() {
		return this.x;
	}

	public int getY() {
		return this.y;
	}

	public int getWidth() {
		return this.width;
	}

	public int getHeight() {
		return this.height;
	}

	public void setPos(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public void setDimension(int width, int height) {
		this.width = width;
		this.height = height;
	}
}